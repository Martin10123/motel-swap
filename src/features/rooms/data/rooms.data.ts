// Función auxiliar para generar horarios del día
const generateTimeSlots = (currentHour: number = 14): any[] => {
  const slots = [];
  for (let hour = 0; hour < 24; hour += 3) {
    slots.push({
      startTime: `${hour.toString().padStart(2, '0')}:00`,
      endTime: `${((hour + 3) % 24).toString().padStart(2, '0')}:00`,
      available: hour >= currentHour || hour < 6,
    });
  }
  return slots;
};

